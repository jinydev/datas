# 이 파일은 도구를 모아놓은 창고 모듈입니다! (my_tools.py)

def add(a, b):
    """두 수를 더하는 강력한 커스텀 함수"""
    return a + b

def multiply(a, b):
    """두 수를 곱하는 배수기"""
    return a * b

def clean_data(dirty_list):
    """0 이상의 정수만 필터링하여 남기는 커스텀 데이터 전처리기"""
    return list(filter(lambda x: isinstance(x, int) and x > 0, dirty_list))

