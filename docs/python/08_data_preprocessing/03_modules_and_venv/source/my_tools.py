    print("====== my_tools.py 공장이 [직접 실행] 모드로 가동되었습니다! ======")
    
    # 여기서 만든 함수들이 정상 동작하는지 테스트용으로 사용하기 딱 좋습니다.
    print("[1] 덧셈 테스트 (5+7) =", add(5, 7))
    print("[2] 곱셈 테스트 (3*8) =", multiply(3, 8))
    
